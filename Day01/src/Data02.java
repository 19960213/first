public class Data02 {
	public static void main(String[] args) {
		int a=10;
/* a는 자료형 값을 저장하는 변수라 한다. 변수는 단 하나
 * 값만 저장하는 자료구조이다. 가장 마지막에 저장된 값
 * 만 저장된다.
 * 	변수 선언 형식)
 *   타입(자료형) 변수명=값;
 *   ;(세미콜론)은 한 문장의 끝을 뜻한다.
 *   =은 오른쪽 값을 왼쪽 변수에 대입하는 대입(할당)연
 *   산자이다.	
 */
		System.out.println(a);
		
		double b=10.3;//double은 실수 타입(자료형)
		System.out.println("실수값:"+b);//+는 문자열
		//연결연산기호
		boolean re=false;
		System.out.println("논리값:"+re);
	}
}



