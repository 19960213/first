/* 자바의 산술 (수학) 연산자
 * 1. +(덧셈), -(뺄셈), *(곱셈), /(나눗셈),
 * %(나머지)
 */
public class Day03 {

	public static void main(String[] args) {
		int a=9;
		int b=3;
		
		int re=0;
		
		re=a+b;
		System.out.println(a+"+"+b+"="+re);
		
		re=a-b;
		System.out.println(a+"-"+b+"="+re);
		
		re=a*b;
		System.out.println(a+"*"+b+"="+re);
		
		re=a/b;
		System.out.println(a+"/"+b+"="+re);
		
	}

}
